    <p class="stuff" >
        1- Contrôles des connaissances et examens : Référence : Arrête n° : 711 du 03 Novembre 2011
Article 1 : Durant les épreuves de contrôle, les étudiants sont tenus de respecter toutes les directives émanant des enseignants
surveillants.
Article 2 : Aucun étudiant n’est autorisé à participer à une épreuve :
- S’il n’est pas inscrit sur les listes officielles de l’établissement,
- S’il arrive trente minutes après la distribution des sujets,
- S’il n’est pas muni d’une pièce d’identité ou de sa carte d’étudiant.
Article 3 : Aucun étudiant n’est autorisé à quitter la salle d’examen pendant la demi-heure qui suit la distribution des sujets.
Lorsque, pour une raison déterminée l’étudiant sollicite une sotie momentanée, il doit être accompagné par un enseignant
surveillant.
Article 4 : Chaque étudiant doit s’équiper de tout le matériel autorisé qui lui permet de composer dans les meilleures
conditions. Aucun emprunt n’est autorisé sans l’avis préalable d’un enseignant surveillant.
2- Sanctions : Référence : Arrête n° : 371 du 11 Juin 2014
Article 1 : Sont considérées comme infractions du 1er degré:
- toute tentative de fraude, fraude établie ou fraude préméditée établie à un examen,
- tout refus d'obtempérer à des directives émanant de l'administration, du personnel enseignant- chercheur ou de sécurité,
- toute demande non fondée de double correction.
Article 2 : Sont considérées comme infractions du deuxième degré:
- les récidives des infractions du 1er degré,
- l'entrave à la bonne marche de l'établissement, le désordre organisé, la violence, les menaces et voies de faits de toute nature,
- la détention de tout moyen avec l'intention établie de porter atteinte à l'intégrité physique du personnel enseignantschercheurs, du personnel administratif, technique et de service, et des étudiants,
- le faux et usage de faux, la falsification et la substitution de documents pédagogiques et administratifs,
- l'usurpation d'identité,
- la diffamation à l'égard de l'ensemble du personnel universitaire et des étudiants,
- les actions délibérées de perturbation et désordre caractérisés portant atteinte au bon déroulement des activités
pédagogiques telles que les entraves aux enseignements et aux examens ou leur boycott, le regroupement perturbateur...,
- le vol, l'abus de confiance et le détournement de biens de l'établissement, des enseignants et des étudiants,
- la détérioration délibérée des biens de l'établissement : matériels, mobiliers et accessoires,
- les insultes et propos irrévérencieux à l'égard de l'ensemble du personnel – enseignants chercheurs, personnel administratif,
technique et de service - et des étudiants,
- le refus d'obtempérer à un contrôle réglementaire dans l'enceinte de l'établissement.
Article 3 : Les sanctions applicables aux infractions du 1er degré sont fixées comme suit :
- Avertissement verbal,
- Avertissement écrit et versé au dossier pédagogique de l'étudiant,
- Blâme versé au dossier pédagogique de l'étudiant.
En cas de tentative de fraude ou de fraude établie, la note de zéro sur vingt est automatiquement attribuée à l'examen en
cause.
Article 4 : Les sanctions applicables aux infractions du 2ème degré sont fixées comme suit:
- Exclusion de la matière concernée. Cette exclusion entraine de fait la non validation des résultats éventuellement acquis dans
cette matière.
- Exclusion du semestre ou de l'année en cours, suivant que la progression est semestrielle ou annuelle. Cette exclusion entraine
de fait la non validation des résultats éventuellement acquis dans ce semestre ou cette année.
- Exclusion de deux semestres ou de deux années, incluant le semestre ou l'année en cours, suivant que la progression est
semestrielle ou annuelle. Cette exclusion entraine de fait la non validation des résultats éventuellement acquis dans le semestre
ou l'année en cours.
- Exclusion de deux semestres ou de deux années, incluant le semestre ou l'année en cours, suivant que la progression est
semestrielle ou annuelle, dans tout établissement d'enseignement supérieur. Cette exclusion entraine de fait la non validation
des résultats éventuellement acquis dans le semestre ou l'année en cours
